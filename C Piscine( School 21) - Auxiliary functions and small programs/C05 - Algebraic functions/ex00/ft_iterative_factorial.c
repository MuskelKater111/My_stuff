int		ft_iterative_factorial(int nb)
{
	int i;
	int rnb;

	i = 1;
	rnb = 1;
	if (nb < 1)
	{
		return (0);
	}
	while (i <= nb)
	{
		rnb = rnb * i;
		i++;
	}
	return (rnb);
}

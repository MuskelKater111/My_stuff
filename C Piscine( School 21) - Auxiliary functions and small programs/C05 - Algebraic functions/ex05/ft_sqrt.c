int		ft_sqrt(int nb)
{
	unsigned int	i;
	unsigned int	nbu;

	i = 0;
	if (nb <= 0)
	{
		return (0);
	}
	nbu = nb;
	while (i * i < nbu)
	{
		i++;
	}
	if (i * i != nbu)
	{
		return (0);
	}
	return (i);
}

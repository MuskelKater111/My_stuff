int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int x;
	unsigned int y;

	x = 0;
	y = 0;
	while (s1[x] != '\0' && x != n)
	{
		while (s2[y] != '\0' && y != n)
		{
			if (s1[x] != s2[y])
			{
				return (s1[x + 1] - s2[y]);
			}
			else
			{
				y++;
				x++;
			}
		}
	}
	return (s1[x + 1] - s2[y]);
}

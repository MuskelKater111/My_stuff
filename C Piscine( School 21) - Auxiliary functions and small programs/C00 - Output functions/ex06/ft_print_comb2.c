#include <unistd.h>

void		ft_print_comb2(void)
{
	char a[6];

	a[4] = ',';
	a[5] = ' ';
	a[0] = a[1] = a[2] = a[3] = '0';
	while (a[0] <= '9')
	{
		while (a[1] <= '9')
		{
			while (a[2] <= '9')
			{
				while (a[3] <= '9')
				{
					write(1, &a[0], 2);
					write(1, &a[5], 1);
					write(1, &a[2], 2);
					write(1, &a[4], 2);
					a[3]++;
				}
				a[2]++;
				a[3] = '0';
			}
			a[1]++;
			a[2] = '0';
		}
		a[0]++;
		a[1] = '0';
	}
}

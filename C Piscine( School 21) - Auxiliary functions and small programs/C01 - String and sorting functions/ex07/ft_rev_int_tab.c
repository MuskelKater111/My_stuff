void		ft_rev_int_tab(int *tab, int size)
{
	int a;
	int a2;
	int temp;

	a = 0;
	a2 = size - 1;
	while (a < (size / 2))
	{
		temp = tab[a];
		tab[a] = tab[a2];
		tab[a2] = temp;
		a++;
		a2--;
	}
}
